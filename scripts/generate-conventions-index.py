#!/usr/bin/env python3
"""Regenerate `.claude/rules/*-conventions.md` index tables from convention bodies.

- Only updates the region between BEGIN_TABLE / END_TABLE markers.
- Uses each body's single-line `title` frontmatter field.
- Fails on missing title, missing markers, or unknown scopes.
- Does not rewrite unchanged files.

Usage:
    python scripts/generate-conventions-index.py
"""

from __future__ import annotations

import sys
from pathlib import Path

try:
    import frontmatter
except ImportError:
    sys.stderr.write(
        "ERROR: python-frontmatter is required.\n"
        "  Install via pre-commit (auto-managed) or:\n"
        "    pip install 'python-frontmatter==1.1.0'\n"
    )
    sys.exit(2)


REPO_ROOT = Path(__file__).resolve().parents[1]
CONVENTIONS_ROOT = REPO_ROOT / ".claude" / "conventions"
RULES_ROOT = REPO_ROOT / ".claude" / "rules"

BEGIN_MARKER = (
    "<!-- BEGIN_TABLE: generated by scripts/generate-conventions-index.py, "
    "edits inside will be overwritten -->"
)
END_MARKER = "<!-- END_TABLE -->"

# Scope name -> index filename under .claude/rules/.
# To add a scope, create matching entries here, under .claude/conventions/<scope>/,
# and under .claude/rules/<index>.
SCOPE_CONFIG: dict[str, dict[str, str]] = {
    "global": {"index": "conventions.md"},
    "python": {"index": "python-conventions.md"},
    "typescript": {"index": "typescript-conventions.md"},
    "infra": {"index": "infra-conventions.md"},
    "github-actions": {"index": "github-actions-conventions.md"},
    "opencode": {"index": "opencode-conventions.md"},
    "testenv": {"index": "testenv-conventions.md"},
}


class ValidationError(Exception):
    pass


def load_body_entries(scope: str) -> list[tuple[str, str, Path]]:
    """Return each body in a scope as (stem, title, body_path)."""
    scope_dir = CONVENTIONS_ROOT / scope
    if not scope_dir.exists():
        return []
    entries: list[tuple[str, str, Path]] = []
    for md_path in sorted(scope_dir.glob("*.md")):
        post = frontmatter.load(md_path)
        title = post.metadata.get("title")
        if not title or not isinstance(title, str):
            raise ValidationError(
                f"{md_path.relative_to(REPO_ROOT)}: missing 'title' frontmatter field"
            )
        if "\n" in title:
            raise ValidationError(
                f"{md_path.relative_to(REPO_ROOT)}: 'title' must be a single line"
            )
        entries.append((md_path.stem, title.strip(), md_path))
    return entries


def validate_layout() -> None:
    """Validate `.claude/conventions/` directory layout."""
    if not CONVENTIONS_ROOT.exists():
        return  # framework 가 막 만들어진 상태일 수 있음

    # Convention bodies are collected only from registered scope directories.
    # Root-level Markdown files may be operational docs like AGENTS.md.
    for child in CONVENTIONS_ROOT.iterdir():
        if child.is_dir() and child.name not in SCOPE_CONFIG:
            raise ValidationError(
                f".claude/conventions/{child.name}/ is not a registered scope. "
                f"Add '{child.name}' to SCOPE_CONFIG in "
                "scripts/generate-conventions-index.py "
                f"and create the matching index in .claude/rules/."
            )


def render_table(entries: list[tuple[str, str, Path]], scope: str) -> str:
    lines = ["| Convention | File |", "| --- | --- |"]
    for stem, title, body_path in entries:
        rel = body_path.relative_to(REPO_ROOT).as_posix()
        escaped = title.replace("|", r"\|")
        lines.append(f"| {escaped} | [{stem}]({rel_to_index(rel, scope)}) |")
    return "\n".join(lines) + "\n"


def rel_to_index(body_rel_to_repo: str, scope: str) -> str:
    """Return the relative path from an index file to a convention body.

    Index files live under `.claude/rules/X.md`; bodies live under
    `.claude/conventions/<scope>/Y.md`, so the path always has the form
    `../conventions/<scope>/Y.md`.
    """
    return f"../conventions/{scope}/{Path(body_rel_to_repo).name}"


def replace_table_region(index_text: str, new_table: str, index_path: Path) -> str:
    begin_idx = index_text.find(BEGIN_MARKER)
    end_idx = index_text.find(END_MARKER)
    if begin_idx == -1 or end_idx == -1:
        raise ValidationError(
            f"{index_path.relative_to(REPO_ROOT)}: missing "
            "BEGIN_TABLE / END_TABLE markers"
        )
    if end_idx < begin_idx:
        raise ValidationError(
            f"{index_path.relative_to(REPO_ROOT)}: END_TABLE appears before BEGIN_TABLE"
        )
    head = index_text[: begin_idx + len(BEGIN_MARKER)]
    tail = index_text[end_idx:]
    return f"{head}\n\n{new_table}\n{tail}"


def regenerate_scope(scope: str, index_filename: str) -> bool:
    """Regenerate one scope index and return whether it changed."""
    index_path = RULES_ROOT / index_filename
    if not index_path.exists():
        raise ValidationError(
            f"{index_path.relative_to(REPO_ROOT)}: index file not found "
            f"for scope '{scope}'"
        )

    entries = load_body_entries(scope)
    new_table = render_table(entries, scope)
    original = index_path.read_text(encoding="utf-8")
    updated = replace_table_region(original, new_table, index_path)
    if updated != original:
        index_path.write_text(updated, encoding="utf-8")
        return True
    return False


def main() -> int:
    try:
        validate_layout()
    except ValidationError as exc:
        sys.stderr.write(f"validation failed: {exc}\n")
        return 1

    changed = []
    for scope, cfg in SCOPE_CONFIG.items():
        try:
            if regenerate_scope(scope, cfg["index"]):
                changed.append(scope)
        except ValidationError as exc:
            sys.stderr.write(f"validation failed: {exc}\n")
            return 1

    if changed:
        sys.stdout.write("regenerated index for scopes: " + ", ".join(changed) + "\n")
    return 0


if __name__ == "__main__":
    sys.exit(main())
